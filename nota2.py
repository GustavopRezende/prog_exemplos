# O símbolo hash(cerquilha ou jogo da velha) orienta o interpretador Python a ignorar os caracteres da linha
# Usado para definir COMENTÁRIOS - esclarecem trechos do código, facilitando a sua LEGIBILIDADE
# Estão relacionados diretamente com a QUALIDADE do código, pois facilitam a manutenção

# Em Python, os nomes de variáveis também devem obedecer a algumas regras simples: 
# Um nome de variável é uma sequência de letras (a → z, A → Z) e números (0 → 9), 
# que devem sempre começar com uma letra. Apenas letras comuns são permitidas. 
# Letras acentuadas, cedilhas, espaços, caracteres especiais como $, #, @, etc.
# são proibidos, exceto para o caractere _ (sublinhado/underline).

